
public class Switch {

	public static void main(String[] args) {
		int a = 10;
		
		switch(a) {
		case 1:
			System.out.print("vale 1!");
			break;
		case 5:
			System.out.print("vale 5!");
		case 10:
			System.out.print("vale 10!");
		case 15: 
			System.out.print("vale 15!");
			break;
		default:
			System.out.print("default!");
		}
	}

}
