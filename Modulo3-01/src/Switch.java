
public class Switch {

	public static void main(String[] args) {
		int a = 10;
		
		switch(a) {
		case 1:
			System.out.println("vale 1!");
			break;
		case 5:
			System.out.println("vale 5!");
			break;
		case 10:
			System.out.println("vale 10!");
			break;
		case 15: 
			System.out.println("vale 15!");
			break;
		default:
			System.out.println("default!");
		}
	}

}
